function printLocations(model)
	for k,v in pairs(ents.FindByModel(model)) do
		// Temp way to detect user spawned props
		if v._id == 15 then
			local pos = v:GetPos()
			local ang = v:GetAngles()
			print("{Position = Vector(" ..math.ceil(pos.x) ..", " ..math.ceil(pos.y) ..", " ..math.ceil(pos.z) .."), Angles = Angle(" ..math.ceil(ang.p) ..", " ..math.ceil(ang.y) ..", " ..math.ceil(ang.r) ..")},")
		end
	end
end

